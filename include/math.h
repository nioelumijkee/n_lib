//----------------------------------------------------------------------------//
#define AF_INTFRACT(IN, INT0_I, INT1_I, FRACT_F)	\
  (INT0_I) = (IN);                                  \
  (INT1_I) = (INT0_I) + 1;                          \
  (FRACT_F) = (IN) - (INT0_I);

//----------------------------------------------------------------------------//
#define AF_ABS(IN, OUT)   \
  if ((IN) < 0)           \
    (OUT) = 0 - (IN);	  \
  else                    \
    (OUT) = (IN);

//----------------------------------------------------------------------------//
// floor (round down)
#define AF_FLOOR(IN, OUT_I)                     \
  (OUT_I) = (IN);

//----------------------------------------------------------------------------//
// ceil (round up)
#define AF_CEIL(IN, OUT_I)			\
  if ((IN) < 0)                     \
    (OUT_I) = (IN) - 0.5;			\
  else                              \
    (OUT_I) = (IN) + 0.5;

//----------------------------------------------------------------------------//
// wrap (-0.5 ... 0.5)
#define AF_WRAP(IN, BUF_I, OUT)     \
  if ((IN) < 0)                     \
    (BUF_I) = (IN) - 0.5;			\
  else                              \
    (BUF_I) = (IN) + 0.5;           \
  (OUT) = (IN) - (BUF_I);

