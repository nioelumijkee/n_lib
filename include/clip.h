//----------------------------------------------------------------------------//
#define AF_CLIP_MINMAX(MIN, MAX, IN) \
  if ((IN) < (MIN))                  \
    (IN) = (MIN);                    \
  else if ((IN) > (MAX))             \
    (IN) = (MAX);

//----------------------------------------------------------------------------//
#define AF_CLIP_MIN(MIN, IN) \
  if ((IN) < (MIN))          \
    (IN) = (MIN);

//----------------------------------------------------------------------------//
#define AF_CLIP_MAX(MAX, IN) \
  if ((IN) > (MAX))          \
    (IN) = (MAX);
